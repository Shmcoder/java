public class Object {
    String NAME, PLACE;
    int AGE;

    public static void main(String[] args) {
        Object obj1 = new Object();
        obj1.NAME = "SHYAM";
        obj1.AGE = 20;
        obj1.PLACE = "SIVAGANGAI";
        System.out.println(obj1.NAME);
        System.out.println(obj1.AGE);
        System.out.println(obj1.PLACE);
    }
}
